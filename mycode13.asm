ORG 100H
.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 13,10,"Ingrese una cadena de caracteres: $"
MSG2 DB 13,10,"Cadena: $"
CADENA DB 16 DUP(0)
.CODE
INICIO:
MOV AX, @DATA
MOV DS, AX

LEA DX, MSG1
MOV AH, 09H
INT 21H 

MOV CL, 06H
MOV SI, 0

LEE:
MOV AH, 00H
INT 16H
MOV CADENA[SI], AL
INC SI
LOOP LEE

MOV AH, 2
MOV DL, 10
INT 21H

MOV DL, 13
INT 21H

LEA DX, MSG2
MOV AH, 09H
INT 21H

MOV CL, 06
MOV SI, 0
IMPRIMIR:
MOV AH, 02H
MOV DL, CADENA[SI]
INT 21H
INC SI
LOOP IMPRIMIR
       
MOV AH, 4CH       
INT 21H
END INICIO
